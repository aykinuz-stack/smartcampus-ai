def get_edebiyat11_reference(topic: str) -> list:
    """Verilen konuya en yakin edebiyat 11 referanslarini dondurur."""
    import difflib
    results = []
    topic_lower = topic.lower()
    for key, val in EDEBIYAT_11_REFERANS.items():
        searchable = f"{val['baslik']} {val['unite']} {val['icerik'][:500]}".lower()
        ratio = difflib.SequenceMatcher(None, topic_lower, searchable).ratio()
        if ratio > 0.25 or any(w in searchable for w in topic_lower.split() if len(w) > 3):
            results.append((ratio, key, val))
    results.sort(key=lambda x: x[0], reverse=True)
    return results[:5]

def get_all_edebiyat11_keys() -> list:
    """Tum edebiyat 11 referans anahtarlarini dondurur."""
    return list(EDEBIYAT_11_REFERANS.keys())
