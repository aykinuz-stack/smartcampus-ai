def get_din_kulturu12_reference(topic: str) -> list:
    """Verilen konuya en yakin din kulturu 12 referanslarini dondurur."""
    import difflib
    results = []
    topic_lower = topic.lower()
    for key, val in DIN_KULTURU_12_REFERANS.items():
        searchable = f"{val['baslik']} {val['unite']} {val['icerik'][:500]}".lower()
        ratio = difflib.SequenceMatcher(None, topic_lower, searchable).ratio()
        if ratio > 0.25 or any(w in searchable for w in topic_lower.split() if len(w) > 3):
            results.append((ratio, key, val))
    results.sort(key=lambda x: x[0], reverse=True)
    return results[:5]

def get_all_din_kulturu12_keys() -> list:
    """Tum din kulturu 12 referans anahtarlarini dondurur."""
    return list(DIN_KULTURU_12_REFERANS.keys())
