def get_edebiyat10_reference(topic: str) -> list:
    """Verilen konuya en uygun edebiyat 10 referans kayitlarini dondurur."""
    import difflib
    results = []
    topic_lower = topic.lower()
    for key, val in EDEBIYAT_10_REFERANS.items():
        searchable = f"{val['baslik']} {val['unite']} {val['icerik'][:500]}".lower()
        ratio = difflib.SequenceMatcher(None, topic_lower, searchable).ratio()
        if ratio > 0.25 or any(w in searchable for w in topic_lower.split() if len(w) > 3):
            results.append((ratio, key, val))
    results.sort(key=lambda x: x[0], reverse=True)
    return results[:5]


def get_all_edebiyat10_keys() -> list:
    """Tum edebiyat 10 referans anahtarlarini dondurur."""
    return list(EDEBIYAT_10_REFERANS.keys())
